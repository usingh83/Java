package day1.examples;

public class ExampleSwitch {

	public static void main(String[] args) {
		
		int j=2;
		
		switch(j){
		case 1:
			System.out.println("value is 1");
			break;
		case 2:
			System.out.println("Value is 2");
			break;
		case 3:
			System.out.println("Value is 3");
			break;
		}

	}

}
